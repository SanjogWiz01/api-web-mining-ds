## ✨ Features & Highlights
- **API Mastery**: REST, GraphQL, authentication (OAuth, tokens), pagination, rate limiting.
- **Web Mining**: Ethical scraping with BeautifulSoup, Scrapy, Selenium, Playwright.
- **Data Pipeline**: Extraction → Cleaning → Storage (Pandas, SQL, NoSQL).
- **Real-World Projects**: Weather, social media, e-commerce, news APIs + mining.
- **Modern Best Practices**: Async (aiohttp), error handling, logging, ethics & legality.

**Perfect for**: Data Scientists, ML Engineers, Analysts, Students (Pokhara University & beyond).

